const fs = require('fs');
const path = require('path');

// Cria pasta se não existir
const dir = path.join('app', 'articles', '[slug]');
if (!fs.existsSync(dir)) fs.mkdirSync(dir, { recursive: true });

const content = [
  "import { getArticleBySlug, getAllArticles } from '@/lib/articles';",
  "import { MDXRemote } from 'next-mdx-remote/rsc';",
  "import { notFound } from 'next/navigation';",
  "import Link from 'next/link';",
  "",
  "export async function generateStaticParams() {",
  "  const articles = getAllArticles();",
  "  return articles.map((a) => ({ slug: a.slug }));",
  "}",
  "",
  "export async function generateMetadata({ params }) {",
  "  const { slug } = await params;",
  "  const article = getArticleBySlug(slug);",
  "  if (!article) return {};",
  "  return {",
  "    title: article.title + ' | The Little Raccoon',",
  "    description: article.description,",
  "  };",
  "}",
  "",
  "const tagColors = {",
  "  Build: '#8A7A50',",
  "  Guide: '#5A8A70',",
  "  Survival: '#9A5858',",
  "  Seeds: '#7A9A50',",
  "  Redstone: '#8A5A5A',",
  "  Tips: '#6A7A8A',",
  "  Farms: '#7A8A5A',",
  "};",
  "",
  "export default async function ArticlePage({ params }) {",
  "  const { slug } = await params;",
  "  const article = getArticleBySlug(slug);",
  "  if (!article) notFound();",
  "  const tagColor = tagColors[article.tag] || '#5A8A70';",
  "  return (",
  "    <main style={{ minHeight: '100vh', backgroundColor: '#0B1410', paddingBottom: 80 }}>",
  "      <div style={{ backgroundColor: '#0C1810', borderBottom: '1px solid #1A2E1A', padding: '48px 40px 40px' }}>",
  "        <div style={{ maxWidth: 800, margin: '0 auto' }}>",
  "          <Link href='/articles' style={{ fontSize: 11, fontWeight: 700, textTransform: 'uppercase', letterSpacing: '0.18em', color: '#C4B47E', textDecoration: 'none', opacity: 0.7 }}>",
  "            back to Articles",
  "          </Link>",
  "          <div style={{ marginTop: 24 }}>",
  "            <span style={{ display: 'inline-block', padding: '4px 10px', fontSize: 10, fontWeight: 700, textTransform: 'uppercase', letterSpacing: '0.14em', color: tagColor, backgroundColor: '#0A120880', border: '1px solid ' + tagColor + '50', borderRadius: 4, marginBottom: 16 }}>{article.tag}</span>",
  "            <h1 style={{ margin: '0 0 16px', fontFamily: 'var(--font-oswald)', fontSize: 'clamp(1.8rem, 4vw, 2.8rem)', fontWeight: 700, color: '#EDE6D6', lineHeight: 1.15, letterSpacing: '0.02em' }}>{article.title}</h1>",
  "            <p style={{ margin: 0, fontSize: 12, color: '#5A6A55', fontWeight: 500, letterSpacing: '0.08em' }}>",
  "              {new Date(article.date).toLocaleDateString('en-US', { year: 'numeric', month: 'long', day: 'numeric' })}",
  "            </p>",
  "          </div>",
  "        </div>",
  "      </div>",
  "      <div style={{ maxWidth: 800, margin: '0 auto', padding: '48px 40px 0' }}>",
  "        <div className='article-content'>",
  "          <MDXRemote source={article.content} />",
  "        </div>",
  "        <div style={{ marginTop: 64, paddingTop: 32, borderTop: '1px solid #1A2E1A', textAlign: 'center' }}>",
  "          <p style={{ color: '#5A6A55', fontSize: 12, marginBottom: 16 }}>Enjoyed this guide?</p>",
  "          <Link href='/articles' style={{ display: 'inline-block', padding: '12px 36px', fontSize: 10, fontWeight: 700, textTransform: 'uppercase', letterSpacing: '0.18em', color: '#C4B47E', border: '1px solid #1A2E1A', borderRadius: 8, textDecoration: 'none' }}>Browse All Articles</Link>",
  "        </div>",
  "      </div>",
  "    </main>",
  "  );",
  "}",
].join('\n');

fs.writeFileSync(path.join(dir, 'page.tsx'), content, 'utf8');
console.log('page.tsx criado com sucesso!');

